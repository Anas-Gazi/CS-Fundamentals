#include<stdio.h>
int main(){
  int i=1;
  while(i<=10){
    printf("%d\n",i);
    i++;
  }
}

//This program uses a while loop to print the numbers from 1 to 10. The variable i is initialized to 1, and the loop continues as long as i is less than or equal to 10. Inside the loop, the current value of i is printed, and then i is incremented by 1.